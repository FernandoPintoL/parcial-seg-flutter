     */
    public function downloadFlutterProject(Request $request)
    {
        // Validate the request
        /*$validated = $request->validate([
            'code' => 'required|string',
            'project_name' => 'required|string|max:255',
            'id' => 'id:nullable|exists:pizarras,id',
